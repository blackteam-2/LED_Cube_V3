//-----------------------------------------------------------------------------
void	Effect_Rain(int iterations, int itterationDelay) {
	int iterCnt, i, j;
	byte xx, yy;

	SetAllLEDSOff();

	for(iterCnt=0; iterCnt<iterations; iterCnt++) {
		ShiftPlane(Plane_XY, 1, false, true);
		j = random(10, 20);
		for(i=0; i<j; i++) {
			xx = random(0, 8);
			yy = random(0, 8);
			SetPixel(xx, yy, 7, true);
			}
		delay(itterationDelay);
		}
	}
